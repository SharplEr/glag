package org.sharpler.glag.output;

import static java.nio.file.StandardOpenOption.APPEND;

import com.google.errorprone.annotations.FormatMethod;
import com.google.errorprone.annotations.FormatString;
import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.Comparator;
import java.util.HashSet;
import java.util.List;
import java.util.Objects;
import org.fusesource.jansi.AnsiConsole;
import org.sharpler.glag.aggregations.RuntimeEvents;
import org.sharpler.glag.distribution.CumulativeDistributionPoint;
import org.sharpler.glag.records.SafepointLogRecord;
import org.sharpler.glag.records.SingleVMOperation;
import org.sharpler.glag.util.TimeUtils;

public final class MdOutput {
    private static final Path DOCS_PATH = Path.of("docs");

    private final Path output;

    public MdOutput(Path output) {
        this.output = output;
    }

    public void print(RuntimeEvents runtimeEvents, int examples) throws IOException {
        var thresholdMs = runtimeEvents.thresholdMs();
        var safepoints = runtimeEvents.safepointLog();

        Files.deleteIfExists(output);
        Files.createFile(output);
        writef("# Report%n%n");
        writef("This report of gc and safepoint log analysis has been generated by **glag** tool version 1.0-SNAPSHOT.%n%n");

        if (safepoints.hasInsideTimeNs()) {
            writef(
                "Throughput lost due to pauses: %.3f (%%) - %.3f (%%)%n%n",
                safepoints.insideSafepointThroughputLoss(),
                safepoints.totalPauseThroughputLoss()
            );
        } else {
            writef(
                "Throughput lost due to total pauses: %.3f (%%)%n%n",
                safepoints.totalPauseThroughputLoss()
            );
        }
        writef("Average pause period: %.3f sec/op%n%n", safepoints.averagePausePeriodSec());

        if (runtimeEvents.gcName() != null) {
            var name = runtimeEvents.gcName().getName();
            writef("%s has been detected.%n%n", name);
            var gcDescription = DOCS_PATH.resolve("gc").resolve(name + ".md");
            if (docExists(gcDescription)) {
                writef("## %s%n%n", name);
                writeDoc(gcDescription);
                writef("%n%n");
            } else {
                AnsiConsole.err().printf("GC '%s' description is unsupported%n", name);
            }
        } else {
            AnsiConsole.err().println("Failed to detect GC by logs");
        }

        writef("## Safepoints%n%n");
        writeDoc(DOCS_PATH.resolve("safepoint").resolve("safepoint.md"));
        writef("%n%n");
        writeAggregateSection("Total safepoint time", safepoints.aggregate().totalTimeDistribution(), thresholdMs, 2);
        writeAggregateSection("Time inside a safepoint", safepoints.aggregate().insideTimeDistribution(), thresholdMs, 2);
        writeAggregateSection("Cleanup time", safepoints.aggregate().cleanupTimeDistribution(), thresholdMs, 2);
        writeAggregateSection("Time to leave safepoint", safepoints.aggregate().leavingTimeDistribution(), thresholdMs, 2);
        writef("## JVM operations in safepoint%n%n");

        var unknownOperations = new HashSet<String>();
        for (var e : safepoints.byTypes().entrySet()) {
            var events = e.getValue();
            var aggregate = Objects.requireNonNull(safepoints.aggregatesByType().get(e.getKey()));

            writef("### Operation '%s'%n%n", e.getKey());
            var description = DOCS_PATH.resolve("operation").resolve(e.getKey() + ".md");
            if (docExists(description)) {
                writef("#### Description%n%n");
                writeDoc(description);
                writef("%n%n");
            } else {
                if (unknownOperations.add(e.getKey())) {
                    AnsiConsole.err().printf("GC operation '%s' is unknown%n", e.getKey());
                }
            }

            writef("Period: %.3f (sec/op)%n%n", safepoints.totalLogTimeSec() / events.size());
            writeAggregateSection("Total time", aggregate.totalTimeDistribution(), thresholdMs, 4);
            writeAggregateSection("Time inside a safepoint", aggregate.insideTimeDistribution(), thresholdMs, 4);
            writeAggregateSection("Time to safepoint", aggregate.reachingTimeDistribution(), thresholdMs, 4);
            writeAggregateSection("Cleanup time", aggregate.cleanupTimeDistribution(), thresholdMs, 4);
            writeAggregateSection("Time to leave safepoint", aggregate.leavingTimeDistribution(), thresholdMs, 4);

            var slowSingleVmOperations = runtimeEvents.slowSingleVmOperations().getOrDefault(e.getKey(), List.of());

            if (slowSingleVmOperations.isEmpty()) {
                continue;
            }

            var topSlowVmOperations = slowSingleVmOperations
                .stream()
                .map(SingleVMOperation::safepointLog)
                .sorted(Comparator.comparingLong(SafepointLogRecord::totalTimeNs).reversed())
                .limit(examples)
                .toList();

            if (!topSlowVmOperations.isEmpty()) {
                writef("#### Slow individual safepoints: threshold = %d ms, top %d%n%n", thresholdMs, examples);
                writef("```");
                for (var safepointLog : topSlowVmOperations) {
                    writef("%n%s%n", safepointLog.origin());
                }
                writef("```%n%n");
            }
        }

        if (!safepoints.aggregate().reachingTimeDistribution().isEmpty()) {
            writef("## Time to safepoint%n%n");
            writeDoc(DOCS_PATH.resolve("safepoint").resolve("time_to_safepoint.md"));
            writef("%n%n");
            writeAggregateSection("Cumulative distribution", safepoints.aggregate().reachingTimeDistribution(), thresholdMs, 3);
        }

        var slowGcs = runtimeEvents.slowGcs()
            .stream()
            .sorted(Comparator.comparingDouble((org.sharpler.glag.records.GcIteration x) -> x.gcLog().finishTimeSec() - x.gcLog().startTimeSec()).reversed())
            .limit(examples)
            .toList();
        if (!slowGcs.isEmpty()) {
            writef("## GC iterations with long pauses: threshold = %d ms, top %d%n%n", thresholdMs, examples);

            for (var slowGc : slowGcs) {
                writef(
                    "### GC iteration %d (%s)%n%n",
                    slowGc.gcLog().gcNum(),
                    TimeUtils.formatDuration(Math.round((slowGc.gcLog().finishTimeSec() - slowGc.gcLog().startTimeSec()) * 1E9))
                );

                writef("#### Slow safepoints%n%n");
                writef("```");
                for (var safepoint : slowGc.safepointLog()) {
                    writef("%n%s%n", safepoint.origin());
                }
                writef("```%n%n");

                writef("#### Gc logs%n%n");
                writef("```%n");
                for (var gcEvent : slowGc.gcLog().records()) {
                    writef("%s%n", gcEvent.origin());
                }
                writef("```%n%n");
            }
        }
        var slowSimultaneousGcs = runtimeEvents.slowSimultaneousGcs();
        if (!slowSimultaneousGcs.isEmpty()) {
            writef("## Simultaneous GC iterations with long pauses: threshold = %d ms%n%n", thresholdMs);

            for (var slowSimultaneousGc : slowSimultaneousGcs) {
                writef(
                    "### GC iterations %s%n%n",
                    slowSimultaneousGc.gcs().stream()
                        .map(gc -> "%d (%s)".formatted(
                            gc.gcNum(),
                            TimeUtils.formatDuration(Math.round((gc.finishTimeSec() - gc.startTimeSec()) * 1E9))
                        ))
                        .toList()
                );

                writef("#### Slow safepoints%n%n");
                writef("```");
                for (var safepoint : slowSimultaneousGc.safepointLog()) {
                    writef("%n%s%n", safepoint.origin());
                }
                writef("```%n");


                for (var slowGc : slowSimultaneousGc.gcs()) {
                    writef("#### GC %d logs%n%n", slowGc.gcNum());
                    writef("```%n");
                    for (var gcEvent : slowGc.records()) {
                        writef("%s%n", gcEvent.origin());
                    }
                    writef("```%n%n");
                }
            }
        }

    }

    @FormatMethod
    private void writef(@FormatString String format, Object... args) throws IOException {
        Files.writeString(output, String.format(format, args), APPEND);
    }

    private void writeAggregateSection(
        String title,
        List<CumulativeDistributionPoint> points,
        int thresholdMs,
        int headingLevel
    ) throws IOException {
        if (points.isEmpty()) {
            return;
        }

        writef("%s %s%n%n", "#".repeat(headingLevel), title);
        writef("| Timing (ms) | Probability (%%)|%n");
        writef("| ----------- | -------------- |%n");
        for (var point : points) {
            var timingMs = point.value() / 1E6;
            writef(
                timingMs > thresholdMs ? "| **%.3f** | **%.2f** |%n" : "| %.3f | %.2f |%n",
                timingMs,
                point.prob() * 100d
            );
        }
        writef("%n");
    }

    private boolean docExists(Path docPath) throws IOException {
        try (var stream = getClass().getClassLoader().getResourceAsStream(docPath.toString())) {
            return stream != null;
        }
    }

    private void writeDoc(Path docPath) throws IOException {
        var strPath = docPath.toString();

        byte[] bytes;
        try (var stream = getClass().getClassLoader().getResourceAsStream(strPath)) {
            if (stream == null) {
                throw new IllegalStateException("Can't find document in resources: '%s'".formatted(strPath));
            }

            bytes = stream.readAllBytes();
        }
        if (bytes.length == 0) {
            throw new IllegalStateException("Can't read document in resources: '%s'".formatted(strPath));
        }
        Files.write(output, bytes, APPEND);
    }
}
